package coursework;

public class ScoreEntry {
 private String profileName;
 private int score;

public int getScore() {
	return score;
}
public void setScore(int score) {
	this.score = score;
}
public String getProfileName() {
	return profileName;
}
public void setProfileName(String profileName) {
	this.profileName = profileName;
}
}
